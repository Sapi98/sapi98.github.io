const categoryOrder = ["book", "journal", "conference", "thesis", "preprint"];
const categoryLabels = {
  book: "Book Chapter",
  journal: "Journal Papers",
  conference: "Conference Papers",
  thesis: "Thesis and Dissertations",
  preprint: "Preprints"
};

function sortPubs(pubs) {
  return [...pubs].sort((a, b) => (b.year - a.year) || a.title.localeCompare(b.title));
}

function renderAllSummary() {
  return `
    <section class="pub-total-block">
      <div>
        <p class="eyebrow">All categories</p>
        <h3>All Publications</h3>
      </div>
      <span>${publications.length} total publications</span>
    </section>
  `;
}

function highlightMyName(authors) {
  return authors.replaceAll("S. Biswas", "<strong>S. Biswas</strong>");
}

function renderCard(pub, index) {
  const view = pub.link && pub.link !== "#"
    ? `<a class="btn btn-small btn-soft" href="${pub.link}" target="_blank" rel="noopener">View</a>`
    : "";

  return `
    <article class="publication-card" data-type="${pub.type}">
      <div class="pub-topline">
        <span class="pub-number">${index}</span>
        <span class="pub-meta">${categoryLabels[pub.type]}</span>
      </div>
      <h3>${pub.title}</h3>
      <p class="pub-authors">${highlightMyName(pub.authors)}</p>
      <p class="pub-venue">${pub.venue}</p>
      <div class="pub-actions">
        ${view}
        <button class="btn btn-small btn-soft bib-toggle" type="button">BibTeX</button>
      </div>
      <pre class="bibtex" hidden>${pub.bibtex || ""}</pre>
    </article>
  `;
}

function renderCategory(type, pubs) {
  const sorted = sortPubs(pubs);

  return `
    <section class="pub-category-block" data-category="${type}">
      <div class="pub-category-heading">
        <h3>${categoryLabels[type]}</h3>
        <span>${sorted.length} total</span>
      </div>
      <div class="publication-list nested">
        ${sorted.map((pub, index) => renderCard(pub, index + 1)).join("")}
      </div>
    </section>
  `;
}

function renderPublications(filter = "all") {
  const container = document.getElementById("publicationList");
  if (!container) return;

  container.innerHTML = filter === "all"
    ? renderAllSummary() + categoryOrder
      .map(type => renderCategory(type, publications.filter(pub => pub.type === type)))
      .join("")
    : renderCategory(filter, publications.filter(pub => pub.type === filter));

  setupBibToggles();
}

function setupPublicationFilters() {
  document.querySelectorAll(".filter-btn").forEach(button => {
    button.addEventListener("click", () => {
      document.querySelectorAll(".filter-btn").forEach(item => item.classList.remove("active"));
      button.classList.add("active");
      renderPublications(button.dataset.filter);
    });
  });
}

function setupBibToggles() {
  document.querySelectorAll(".bib-toggle").forEach(button => {
    button.onclick = () => {
      const pre = button.closest(".publication-card").querySelector(".bibtex");
      pre.hidden = !pre.hidden;
      button.textContent = pre.hidden ? "BibTeX" : "Hide BibTeX";
    };
  });
}

document.addEventListener("DOMContentLoaded", () => {
  renderPublications();
  setupPublicationFilters();
});
